package laszlo.karsai.diploma.work;

public class PowerData {
	double powerInWatt;
	
	public PowerData(double powerInWatt) {
		this.powerInWatt = powerInWatt;
	}
	public double getPowerInWatt() {
		return powerInWatt;
	}
	public void setPowerInWatt(double powerInWatt) {
		this.powerInWatt = powerInWatt;
	}	
}
